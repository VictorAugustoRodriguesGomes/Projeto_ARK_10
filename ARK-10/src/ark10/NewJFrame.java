    public void ExibirInformaçõesParaLocação() {
        if (this.InputC.getText().trim().equals("")) {
            String[] colunas = new String[]{
                "Códico", "Nome", "Marca", "Modelo", "Preço", "Valor de Locação", "Dias de Locação", "Valor Total"
            };
            String[][] data = new String[listaCarros.size()][6];

            for (int i = 0; i < listaCarros.size(); i++) {
                data[i] = listaCarros.get(i).ExibirInformaçoes();
            }
            TableModel modelo = new DefaultTableModel(data, colunas);
            jTable2.setModel(modelo);

        } else {
            String[] colunas = new String[]{
                "Códico", "Nome", "Marca", "Modelo", "Preço", "Valor de Locação", "Dias de Locação", "Valor Total"
            };
            String[][] data = new String[listaCarros.size()][6];

            for (int i = 0; i < listaCarros.size(); i++) {
                if (this.InputC.getText().trim().equals(listaCarros.get(i).getCódico())) {
                    data[i] = listaCarros.get(i).ExibirInformaçoes();
                }
            }
            TableModel modelo = new DefaultTableModel(data, colunas);
            jTable2.setModel(modelo);
        }
    }

