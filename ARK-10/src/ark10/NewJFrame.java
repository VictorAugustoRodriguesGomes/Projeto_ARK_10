    private void ADDCarrosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ADDCarrosActionPerformed
        msgcodico.setText("");
        msgNome.setText("");
        msgModelo.setText("");
        msgPreço.setText("");
        msgValorDeLocação.setText("");
        msgDiasDeLocação.setText("");

        try {
            this.códico = this.InputCodico.getText().trim();
            this.nome = this.InputNome.getText().trim();
            this.modelo = this.InputModelo.getText().trim();
            this.preço = Float.parseFloat(this.InputPreço.getText().trim());
            this.valorDeLocação = Float.parseFloat(this.InputValorDeLocação.getText().trim());
            this.diasDeLocação = Integer.parseInt(this.InputDiasDeLocação.getText().trim());

            if (this.op1.isSelected()) {
                this.marca = "Sedan";
            }
            if (this.op2.isSelected()) {
                this.marca = "Fiat";
            }
            exibirValorTotal();

            this.InputCodico.setText("");
            this.InputNome.setText("");
            this.InputModelo.setText("");
            this.InputPreço.setText("");
            this.InputValorDeLocação.setText("");
            this.InputDiasDeLocação.setText("");
            this.InputTotalDaLocação.setText("0.0");

            carro = new Carro(códico, nome, marca, modelo, preço, valorDeLocação, diasDeLocação, ValorTotal);
            listaCarros.add(carro);

            this.ExibirInformaçõesNaTabela();
            this.ExibirInformaçõesParaLocação();
            this.opComboBox();

        } catch (Exception e) {
            if (this.InputCodico.getText().trim().equals("")) {
                msgcodico.setText("* Informe um Codico parra o Carro !*");
            }
            if (this.InputNome.getText().trim().equals("")) {
                msgNome.setText("* Informe um Nome para o Carro !*");
            }
            if (this.InputModelo.getText().trim().equals("")) {
                msgModelo.setText("* Informe um Modelo para o Carro !*");
            }
            msgPreço.setText("* Informe um Preço para o Carro, Só Serão Aceitos Valores Numericos nesse Campo !*");
            msgValorDeLocação.setText("* Informe o Valor de Locação para o Carro, Só Serão Aceitos Valores Numericos nesse Campo !*");
            msgDiasDeLocação.setText("* Informe a Quantidades de Dias de Locação do Carro, Só Serão Aceitos Valores Numericos nesse Campo !*");

        }

    }//GEN-LAST:event_ADDCarrosActionPerformed

